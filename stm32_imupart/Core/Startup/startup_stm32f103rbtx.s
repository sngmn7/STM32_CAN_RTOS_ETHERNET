  {
     uint8_t rx[32];
      uint16_t len = BNO_ReadPacket(rx, sizeof(rx));

      // channel 3(Input Report) + Report ID 0x05(Rotation Vector) 확인
      if (len >= 14 && rx[2] == 3 && rx[4] == 0x05)
      {
        // rx[8..9]=i, rx[10..11]=j, rx[12..13]=k, rx[14..15]=real (각각 리틀엔디안 int16, Q14)
        TxHeader.StdId = 0x201;   // IMU 데이터용 새 ID
        TxHeader.DLC = 8;

        TxData[0] = rx[8];  TxData[1] = rx[9];    // i
        TxData[2] = rx[10]; TxData[3] = rx[11];   // j
        TxData[4] = rx[12]; TxData[5] = rx[13];   // k
        TxData[6] = rx[14]; TxData[7] = rx[15];   // real

        if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox) != HAL_OK)
        {
          Error_Handler();
        }
      }

      HAL_Delay(20);   // 50Hz 정도 주기
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

