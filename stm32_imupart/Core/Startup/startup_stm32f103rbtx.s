#define BNO085_ADDR (0x4A << 1)

// SHTP 패킷 하나 읽기 (헤더 4바이트 먼저 읽고, 길이만큼 이어서 읽기)
static uint16_t BNO_ReadPacket(uint8_t *buf, uint16_t maxlen)
{
  uint8_t header[4];
  if (HAL_I2C_Master_Receive(&hi2c1, BNO085_ADDR, header, 4, 50) != HAL_OK)
    return 0;

  uint16_t len = ((header[1] & 0x7F) << 8) | header[0];
  if (len == 0 || len > maxlen) return 0;

  // 헤더 포함해서 전체 다시 읽기 (STM32 I2C는 재요청 필요)
  if (HAL_I2C_Master_Receive(&hi2c1, BNO085_ADDR, buf, len, 50) != HAL_OK)
    return 0;

  return len;  // buf[0..3]=헤더, buf[4..]=payload
}

// Rotation Vector 리포트 켜기 (채널2, Set Feature Command 0xFD)
static void BNO_EnableRotationVector(void)
{
  uint8_t pkt[21] = {0};
  uint16_t len = 21;

  pkt[0] = len & 0xFF;
  pkt[1] = (len >> 8) & 0x7F;
  pkt[2] = 2;              // channel 2 = control
  pkt[3] = bno_seq_ctrl++;

  pkt[4] = 0xFD;            // Set Feature Command
  pkt[5] = 0x05;            // Report ID: Rotation Vector
  pkt[6] = 0;                // flags
  pkt[7] = 0; pkt[8] = 0;    // change sensitivity
  // report interval 20000us = 50Hz, little-endian 4바이트
  pkt[9]  = 0x20; pkt[10] = 0x4E; pkt[11] = 0x00; pkt[12] = 0x00;
  // batch interval, sensor-specific config = 0

  HAL_I2C_Master_Transmit(&hi2c1, BNO085_ADDR, pkt, len, 100);
}
