  if (i2c_status != HAL_OK)
  {
    Error_Handler();
  }

  HAL_Delay(200);           // 전원 안정화 대기

  // 부팅 시 자동으로 오는 Advertisement 패킷들 비우기
  uint8_t dump[64];
  for (int i = 0; i < 3; i++)
  {
    BNO_ReadPacket(dump, sizeof(dump));
    HAL_Delay(10);
  }

